<p><b>На машине из центра</b>: от станции метро Сокольники двигаемся в сторону 
области по улице Стромынка, проезжаем Матросский Мост, доезжаем до 
метро Преображенская Площадь (пересечение улиц Краснобогатырская, 
Преображенская, Б.Черкизовская и Преображенский вал) и поворачиваем 
налево на улицу Краснобогатырская, едем прямо до ул.3-я Бухвостова и 
поворачиваем налево, перед зданием МОСГОРСУД, доезжаем до первого 
светофора и поворачиваем налево на ул.1-я Бухвостова, едем прямо 350 
метров, до Храма, перед Храмом поворачиваем налево на запасную дорогу, 
осталось проехать 30метров и припарковаться у центральной проходной 
НИИДАР. Заходите в здание, перед постом охраны !!! (у нас нет 
пропускной системы!) повернуть направо, пройти по коридору до ОФИСА № 
115 (1 ЭТАЖ)</p>

