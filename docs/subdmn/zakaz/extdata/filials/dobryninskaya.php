	<h3>Схема проезда</h3>
	<p>
	1. Выходите из метро и поворачиваете налево. Спускаетесь в подземный 
	переход.<br/>
	2. На первом разветвлении держитесь левее.<br/>
	3. Идите прямо до спуска и поворачиваете направо.<br/>
	4. Следуйте по длинному переходу до третьего выхода слева.<br/>
	5. Выходите на Валовую улицу.<br/>
	6. Поворачиваете налево на Валовую улицу по движению автомобилей.<br/>
	7. Через 30 метров, перед рестораном "Траттория", поворачиваете во 
	дворы.<br/>
	8. Заходите во дворы через ворота.<br/>
	9. Слева во дворах будет вход в наш офис (синяя дверь).
	</p>
