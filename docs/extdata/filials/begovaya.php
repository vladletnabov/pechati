<span>Как к нам пройти от станции метро «Полежаевская»?</span>

<p>Для того, чтобы пройти в офис «Печати.RU на Беговой» от станции метро «Полежаевская», нужно приехать на метро в последнем вагоне из центра (или в первом вагоне в сторону центра). Поднимаемся вверх по эскалатору, проходим стеклянные двери, поворачиваем направо, проходим вперед и далее слева по ступенькам поднимаемся вверх в город (при выходе из метро ориентироваться также можно и по указателям «К Магистральным улицам»).<br />
Выйдя на улицу необходимо продолжить свое движение прямо, вдоль Хорошевского шоссе по ходу движения автомобилей (Хорошёвское шоссе будет находиться при ходьбе от Вас по левую руку). Через 80 метров будет перекресток со светофором. Переходим улицу и далее двигаемся прямо до магазина «ДИКСИ» (здание оранжевого цвета). Перед магазином «ДИКСИ» поворачиваем направо и двигаемся вперед, между блочным 12-этажным домом (справа) и пятиэтажным домом из красного кирпича (слева).<br />
Между этими домами видим торец 5-этажки из белого кирпича. Это и есть здание, в котором располагается офис филиала «Печати.RU на Беговой».<br />
Мы находимся в дальнем торце этого здания (Хорошевское шоссе, д. 13А, корпус. 2). Подходим к ближнему к Вам торцу здания. Обходим его слева и идем вдоль подъездов, чтобы они оставались от Вас по правую руку. В конце дома поворачиваем направо и сразу видим вход в наш офис. <br />
Вы добрались. Добро пожаловать!!!</p>

