<p>При заезде с Хорошевского шоссе,<b>от станций метро «Полежаевская» и «Октябрьское Поле»</b>, Вам нужно свернуть на 5-ю Магистральную улицу. Далее нужно проехать вперед (метров 300-350) и свернуть налево, в третий съезд, налево перед «Шиномонтажом» (не доезжая главной дороги метров 50).<br />
