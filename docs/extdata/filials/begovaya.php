<span>Как к нам проехать на личном транспорте?</span>

<p>Наш <b>офис ПЕРЕЕХАЛ</b>, и территориально  мы сейчас <b>находимся между станциями метро "Беговая" и метро "Полежаевская"</b> по адресу: </b>Хорошевское шоссе, д. 13А, корп. 2</b>.<br />
Вход в офис расположен в дальнем, от Хорошевского шоссе, торце здания или в левом торце здания, если смотреть на здание со стороны подъездов.</p>

Заехать на автомобиле к непосредственно к нашему офису «Печати.RU Беговая» в настоящее время возможно только с 5-й Магистральной улицы.
Как  альтернативные варианты парковки, можно рассмотреть еще два варианта:
1) Припарковать Ваш автомобиль у магазина «ДИКСИ» (Хорошевское шоссе, 15) и далее пройти к нам в офис пешком (2 минуты);
2) Припарковать свой автомобиль у магазина «СЕДЬМОЙ КОНТИНЕНТ», при движении со строноы центра (Хорошевское ш., 52, корп.1) и далее перейти дорогу на противоположную и также пройти к нам в офис пешком (3 минуты).

